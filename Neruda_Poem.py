# This is a poem from Pablo Neruda

# English version:
# You can cut all the flowers but you cannot keep spring from coming.
# I grew up in this town,
# my poetry was born between the hill and the river,
# it took its voice from the rain, and like the timber, it steeped itself in the forests.

# Spanish version:
# Podrán cortar todas las flores, pero no podrán detener la primavera.
# Crecí en esta ciudad,
# mi poesía nació entre la colina y el río,
# tomó su voz de la lluvia, y como la madera, se empapó a sí misma en los bosques.

